#!/usr/bin/env python3
"""Create the Zava **Fabric Data Agent (GA)** — idempotently and as code (plan Step 17;
R3 §4 / R4 §5.3). The *"insights beyond Power BI"* pillar: natural-language Q&A over
Zava's governed data via the **Fabric Data Agent REST API**.

What it does
------------
1. Resolves all identifiers from ``deploy_config.json`` (Step 1 schema): the target
   workspace (``workspace.use_existing`` / ``workspace.existing_workspace_id`` /
   ``workspace.name``, created by Step 10), the **Step-14 Direct Lake semantic model**
   (``semantic_model.name`` -> default ``Zava Fleet Analytics``) and the **Step-16
   ontology's auto-created Graph** (``ontology.name`` -> default ``ZavaFleetOntology``;
   the managed Graph is resolved by ``data_agent.graph_name`` or by the single ``Graph``
   item in the workspace). Honours the ``features.enable_data_agent`` gate (default
   ``true``). The **graph source** is included only when ``features.enable_ontology=true``
   AND a Graph item is resolvable. CLI / environment overrides take precedence.
   **No secrets are read from config.**
2. Authenticates via ``DefaultAzureCredential`` (``az login`` / managed identity /
   env service principal), falling back to the Azure CLI
   (``az account get-access-token``) when ``azure-identity`` is absent. A single
   audience is used: the Fabric control plane (``https://api.fabric.microsoft.com``).
3. **Resolves the live artifact ids** of the Step-14 semantic model and the Step-16
   ontology's auto-created Graph (R3 line 358: retrieve real ids via the Fabric REST API
   before assembling the definition) so the data-source bindings point at real items.
4. **Builds the Data Agent item definition** from ``fabric/data-agent/`` (R3 §4.2 format:
   ``Files/Config/data_agent.json``, ``draft/stage_config.json``, per-source
   ``datasource.json`` + ``fewshots.json``, ``publish_info.json``), substituting the
   deploy-time tokens (workspace id, semantic-model / graph artifact ids + display
   names) and mirroring the ``draft/`` parts into a ``published/`` stage (R3 §9: there is
   no separate publish endpoint - deploy a definition containing published parts). Each
   ``datasource.json`` carries the required ``elements`` (``DataSourceElement``) tree with
   ``id`` / ``display_name`` / ``type`` / ``is_selected`` (R3 §4.2); the committed files
   hold schema-valid **placeholder** element ids so offline assembly / ``--dry-run`` stay
   valid.
5. **Find-or-create the Data Agent item** (idempotent) and deploy the definition as code
   via ``POST /v1/workspaces/{id}/dataAgents`` (create) or
   ``POST .../dataAgents/{id}/updateDefinition`` (update). Add retry/backoff + LRO polling.
6. **Reconciles the real ``DataSourceElement`` ids** at deploy time (R3 line 358). The real
   element ids are auto-generated by Fabric when a source is bound, so after the first
   deploy the script calls ``POST .../dataAgents/{id}/getDefinition`` to read them back,
   re-applies the committed ``is_selected`` selection (by ``display_name``), injects the
   real ids into the datasource parts, and calls ``updateDefinition`` again so the
   published definition carries real element ids instead of placeholders. If the tenant
   returns no auto-generated element metadata, the schema-valid placeholders are retained.

Grounding (R3 §2.2 / R4 §5.3)
-----------------------------
* **Semantic model** (``type="semantic_model"``, NL2DAX) — the Step-14 Direct Lake model.
  Example query pairs are **not supported** for semantic models (R3 §5.2); accuracy comes
  from the model's *Prep for AI* configuration, not agent instructions.
* **Graph** (``type="graph"``, NL2GQL) — the Step-16 ontology's **auto-created** managed
  Graph in Fabric (GA). **R4 caveat (documented in code + README):** the Data Agent
  definition REST enum exposes ``graph`` and ``semantic_model`` but **not** ``ontology``,
  so attaching the ontology *directly* (NL2Ontology) may be a **UI-only** step in some
  tenants. The **graph source is the documented programmatic path** to ontology-backed
  reasoning and is what this script wires up.

REST endpoints used (Fabric Data Agent + item-management APIs — R3 §4.1):
    GET    /v1/workspaces/{workspaceId}/items?type=SemanticModel              (resolve semantic-model id)
    GET    /v1/workspaces/{workspaceId}/items?type=Graph                      (resolve graph id)
    POST   /v1/workspaces/{workspaceId}/dataAgents                           (create w/ definition)
    GET    /v1/workspaces/{workspaceId}/dataAgents                           (list / find-by-name)
    POST   /v1/workspaces/{workspaceId}/dataAgents/{id}/updateDefinition     (idempotent update)
    POST   /v1/workspaces/{workspaceId}/dataAgents/{id}/getDefinition        (read back real DataSourceElement ids)

Secondary ALM path (R3 §5.5)
----------------------------
REST is the source-of-truth create here. For repeatable promotion across environments,
the same committed item definition can be deployed with ``fabric-cicd`` / Git integration
+ deployment pipelines (dev -> test -> prod). That ALM wiring is intentionally out of
scope for this create script.

Security / Phase-0 notes
------------------------
* **No secrets.** Identity comes from config (names / ids / placeholders only); auth is
  acquired at runtime via ``DefaultAzureCredential`` / ``az login``. The committed
  definition contains only names, instructions, and placeholder tokens.
* **Authoring phase:** do **not** run this against a live tenant unless you intend to
  create Fabric items. Use ``--dry-run`` to preview every intended REST call with
  **no** authentication and **no** changes.

Usage
-----
    # Preview only - no auth, no mutation (safe):
    python fabric/scripts/70_create_data_agent.py --dry-run

    # Create/refresh the Data Agent from deploy_config.json:
    python fabric/scripts/70_create_data_agent.py

    # Fully explicit:
    python fabric/scripts/70_create_data_agent.py \
        --workspace-name zava-fabric-ws \
        --semantic-model-name "Zava Fleet Analytics" \
        --graph-name ZavaFleetOntology \
        --agent-name "Zava Fleet Insights Agent" \
        --config fabric/config/deploy_config.json
"""

