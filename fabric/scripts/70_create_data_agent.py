def find_or_create_data_agent(
    token: str, workspace_id: str, plan: DataAgentPlan, definition: dict
) -> dict:
    """Find-or-create the Data Agent; update its definition in place if it exists."""
    existing = _find_item_by_name(
        _list_paginated(f"/workspaces/{workspace_id}/dataAgents", token), plan.agent_name
    )
    if existing:
        agent_id = str(existing.get("id"))
        LOG.info(
            "Data Agent %r already exists (%s) - updating definition (idempotent).",
            plan.agent_name, agent_id,
        )
        update_data_agent_definition(token, workspace_id, agent_id, definition)
        return existing
    LOG.info("Creating Data Agent %r ...", plan.agent_name)
    created = _create_item_lro(
        f"/workspaces/{workspace_id}/dataAgents",
        token,
        {
            "displayName": plan.agent_name,
            "description": "Zava Fleet Insights Agent - NL Q&A over the Direct Lake semantic "
                           "model (NL2DAX) and the ontology's auto-created Graph (NL2GQL).",
            "definition": definition,
        },
    )
    LOG.info("Created Data Agent %r (%s).", plan.agent_name, created.get("id"))
    return created


def update_data_agent_definition(
    token: str, workspace_id: str, agent_id: str, definition: dict
) -> None:
    """Deploy an item definition onto an existing Data Agent (idempotent updateDefinition)."""
    status, headers, _ = _request(
        "POST",
        f"{FABRIC_API_BASE}/workspaces/{workspace_id}/dataAgents/{agent_id}/updateDefinition",
        token,
        body={"definition": definition},
    )
    if status == 202:
        _poll_long_running(headers, token)


def reconcile_datasource_elements(
    token: str,
    workspace_id: str,
    agent_id: str,
    *,
    semantic_model_id: str,
    semantic_model_name: str,
    graph_id: Optional[str],
    graph_name: Optional[str],
    include_graph: bool,
) -> bool:
    """Retrieve the real ``DataSourceElement`` ids from the live agent and re-deploy.

    R3 line 358: element ids are auto-generated by Fabric when the source is bound, so we
    read them back via ``getDefinition`` and inject them into the datasource parts before a
    final ``updateDefinition``. Returns True when a reconciled definition was redeployed,
    False when no real elements were retrieved (placeholders retained — definition already
    deployed in the first pass is left in place).
    """
    elements_by_source = retrieve_datasource_elements(token, workspace_id, agent_id)
    if not elements_by_source:
        return False
    parts = build_definition_parts(
        workspace_id=workspace_id,
        semantic_model_id=semantic_model_id,
        semantic_model_name=semantic_model_name,
        graph_id=graph_id,
        graph_name=graph_name,
        include_graph=include_graph,
        elements_by_source=elements_by_source,
    )
    LOG.info(
        "Re-deploying definition with real DataSourceElement ids for source(s): %s.",
        ", ".join(sorted(elements_by_source)),
    )
    update_data_agent_definition(token, workspace_id, agent_id, build_definition(parts))
    return True


