def resolve_workspace_id(token: str, plan: DataAgentPlan) -> str:
    """Resolve the target workspace id (existing path by id, fresh path by display name)."""
    if plan.use_existing_workspace:
        _, _, ws = _request(
            "GET", f"{FABRIC_API_BASE}/workspaces/{plan.existing_workspace_id}", token
        )
        ws_id = str(ws.get("id") or plan.existing_workspace_id)
        LOG.info("Using existing workspace %s (%s).", ws.get("displayName"), ws_id)
        return ws_id
    target = plan.workspace_name.strip().lower()  # type: ignore[union-attr]
    for ws in _list_paginated("/workspaces", token):
        if str(ws.get("displayName", "")).strip().lower() == target:
            LOG.info("Resolved workspace %r -> %s.", plan.workspace_name, ws.get("id"))
            return str(ws["id"])
    raise DataAgentError(
        f"workspace {plan.workspace_name!r} not found - run Step 10 "
        f"(fabric/scripts/00_create_workspace.py) first."
    )


def _find_item_by_name(items: List[dict], name: str) -> Optional[dict]:
    target = name.strip().lower()
    for item in items:
        display = str(item.get("displayName") or item.get("name") or "").strip().lower()
        if display == target:
            return item
    return None


def resolve_semantic_model(token: str, workspace_id: str, name: str) -> Tuple[str, str]:
    """Resolve the Step-14 semantic model item (id, displayName) by display name."""
    items = _list_paginated(
        f"/workspaces/{workspace_id}/items?type={SEMANTIC_MODEL_ITEM_TYPE}", token
    )
    existing = _find_item_by_name(items, name)
    if not existing:
        raise DataAgentError(
            f"semantic model {name!r} not found in workspace {workspace_id} - run Step 14 "
            f"(fabric/scripts/30_create_semantic_model.py) first so the Data Agent can ground "
            f"on the Direct Lake model."
        )
    item_id = str(existing["id"])
    display = str(existing.get("displayName") or existing.get("name") or name)
    LOG.info("Resolved semantic model %r -> %s.", display, item_id)
    return item_id, display


def resolve_graph(
    token: str, workspace_id: str, name: Optional[str]
) -> Optional[Tuple[str, str]]:
    """Resolve the Step-16 ontology's auto-created Graph item (id, displayName).

    Returns None (no error) when no Graph is found, so the agent can still be created
    with the semantic-model source only. When *name* is given, match it; otherwise fall
    back to the single Graph item in the workspace.
    """
    items = _list_paginated(f"/workspaces/{workspace_id}/items?type={GRAPH_ITEM_TYPE}", token)
    if not items:
        LOG.warning(
            "No Graph item found in workspace %s - run Step 16 "
            "(fabric/scripts/60_create_ontology.py) to auto-create the ontology's managed "
            "Graph, or set features.enable_ontology=false. Proceeding with the semantic-model "
            "source only.",
            workspace_id,
        )
        return None
    if name:
        existing = _find_item_by_name(items, name)
        if existing:
            item_id = str(existing["id"])
            display = str(existing.get("displayName") or existing.get("name") or name)
            LOG.info("Resolved Graph %r -> %s.", display, item_id)
            return item_id, display
        LOG.warning(
            "Graph named %r not found in workspace %s (found: %s). Falling back to the single "
            "Graph item if unambiguous.",
            name, workspace_id,
            [str(i.get("displayName") or i.get("name")) for i in items],
        )
    if len(items) == 1:
        only = items[0]
        item_id = str(only["id"])
        display = str(only.get("displayName") or only.get("name") or "")
        LOG.info("Resolved the single Graph item %r -> %s.", display, item_id)
        return item_id, display
    raise DataAgentError(
        f"multiple Graph items found in workspace {workspace_id} "
        f"({[str(i.get('displayName') or i.get('name')) for i in items]}) - set "
        f"data_agent.graph_name (or --graph-name) to disambiguate which Graph the Data Agent "
        f"should ground on."
    )


# ---------------------------------------------------------------------------
# Definition assembly (R3 §4.2 item-definition format)
# ---------------------------------------------------------------------------

def _b64(text: str) -> str:
    return base64.b64encode(text.encode("utf-8")).decode("ascii")


def _read_definition_file(rel_path: str) -> str:
    """Read a committed definition file (path relative to Files/Config/) as raw text."""
    abs_path = os.path.join(DEFINITION_ROOT, *rel_path.split("/"))
    if not os.path.isfile(abs_path):
        raise DataAgentError(f"definition file not found: {abs_path}")
    with open(abs_path, "r", encoding="utf-8") as fh:
        return fh.read()


def _substitute_tokens(
    text: str,
    *,
    workspace_id: str,
    semantic_model_id: str,
    semantic_model_name: str,
    graph_id: Optional[str],
    graph_name: Optional[str],
) -> str:
    """Replace deploy-time tokens in a definition file's text."""
    text = text.replace(TOKEN_WORKSPACE_ID, workspace_id)
    text = text.replace(TOKEN_SEMANTIC_MODEL_ARTIFACT_ID, semantic_model_id)
    text = text.replace(TOKEN_SEMANTIC_MODEL_DISPLAY_NAME, semantic_model_name)
    if graph_id is not None and graph_name is not None:
        text = text.replace(TOKEN_GRAPH_ARTIFACT_ID, graph_id)
        text = text.replace(TOKEN_GRAPH_DISPLAY_NAME, graph_name)
    return text


def _validate_json(part_path: str, text: str) -> None:
    """Fail fast if a substituted definition part is not valid JSON."""
    try:
        json.loads(text)
    except json.JSONDecodeError as exc:
        raise DataAgentError(f"definition part {part_path!r} is not valid JSON after "
                             f"token substitution: {exc}") from exc


def _datasource_source_key(rel_path: str) -> Optional[str]:
    """Return the per-source folder name (e.g. ``semantic_model-ZavaFleetAnalytics``) for a
    ``datasource.json`` part, or ``None`` for any other definition part. The folder name is
    the key used to look up retrieved ``DataSourceElement`` metadata for injection.
    """
    norm = rel_path.replace("\\", "/")
    if not norm.endswith("/datasource.json"):
        return None
    # .../<source-folder>/datasource.json -> <source-folder>
    return norm.rsplit("/", 2)[-2]


def _inject_elements(
    text: str,
    rel_path: str,
    elements_by_source: Optional[Dict[str, List[dict]]],
) -> str:
    """Inject deploy-time-retrieved ``DataSourceElement`` metadata into a datasource part.

    When *elements_by_source* contains real elements for this datasource's source key
    (retrieved from the live agent via ``getDefinition`` — R3 line 358), the static
    placeholder ``elements`` array is replaced with the real one. When no override is
    supplied for this source the committed (schema-valid placeholder) ``elements`` tree is
    kept unchanged, so offline assembly / dry-run still produces a valid definition.
    """
    if not elements_by_source:
        return text
    source_key = _datasource_source_key(rel_path)
    if source_key is None or source_key not in elements_by_source:
        return text
    elements = elements_by_source[source_key]
    if not elements:
        return text
    obj = json.loads(text)
    obj["elements"] = elements
    LOG.info(
        "Injected %d retrieved DataSourceElement(s) into %s (real ids from getDefinition).",
        len(elements), source_key,
    )
    return json.dumps(obj, indent=2)


def build_definition_parts(
    *,
    workspace_id: str,
    semantic_model_id: str,
    semantic_model_name: str,
    graph_id: Optional[str],
    graph_name: Optional[str],
    include_graph: bool,
    elements_by_source: Optional[Dict[str, List[dict]]] = None,
) -> List[dict]:
    """Assemble the Base64 ``definition.parts`` (R3 §4.2).

    Reads the committed ``draft/`` parts (+ top-level ``data_agent.json`` /
    ``publish_info.json``), substitutes deploy-time tokens, injects retrieved
    ``DataSourceElement`` metadata into each ``datasource.json`` when available
    (*elements_by_source*; see :func:`_inject_elements`), and mirrors the draft stage
    into a ``published/`` stage (R3 §9: no separate publish endpoint - deploy a definition
    that contains the published parts). The ``graph-*`` parts are dropped when
    ``include_graph`` is False (ontology disabled / no Graph item).
    """
    # Relative paths (under Files/Config/) of the committed draft definition files.
    draft_rel_paths = [
        "draft/stage_config.json",
        f"draft/{SEMANTIC_MODEL_SOURCE_KEY}/datasource.json",
        f"draft/{GRAPH_SOURCE_KEY}/datasource.json",
        f"draft/{GRAPH_SOURCE_KEY}/fewshots.json",
    ]
    top_level_rel_paths = ["data_agent.json", "publish_info.json"]

    def keep(rel_path: str) -> bool:
        return include_graph or "/graph-" not in f"/{rel_path}"

    parts: List[dict] = []

    def render(rel_path: str) -> str:
        raw = _read_definition_file(rel_path)
        substituted = _substitute_tokens(
            raw,
            workspace_id=workspace_id,
            semantic_model_id=semantic_model_id,
            semantic_model_name=semantic_model_name,
            graph_id=graph_id,
            graph_name=graph_name,
        )
        return _inject_elements(substituted, rel_path, elements_by_source)

    def add_part(rel_path: str) -> None:
        rendered = render(rel_path)
        full_path = f"Files/Config/{rel_path}"
        _validate_json(full_path, rendered)
        parts.append({
            "path": full_path,
            "payload": _b64(rendered),
            "payloadType": "InlineBase64",
        })

    for rel_path in top_level_rel_paths:
        add_part(rel_path)

    for rel_path in draft_rel_paths:
        if not keep(rel_path):
            LOG.info("Dropping definition part %r (graph source disabled).", rel_path)
            continue
        add_part(rel_path)
        # Mirror the draft part into the published stage (R3 §9).
        published_rel = rel_path.replace("draft/", "published/", 1)
        rendered = render(rel_path)
        full_path = f"Files/Config/{published_rel}"
        _validate_json(full_path, rendered)
        parts.append({
            "path": full_path,
            "payload": _b64(rendered),
            "payloadType": "InlineBase64",
        })

    return parts


def build_definition(parts: List[dict]) -> dict:
    """Wrap the parts in the Fabric item-definition envelope."""
    return {"parts": parts}


# ---------------------------------------------------------------------------
# DataSourceElement retrieval / injection (R3 line 358 + §4.2)
# ---------------------------------------------------------------------------
#
# DataSourceElement.id (UUID), display_name, type and is_selected are all REQUIRED by the
# definition schema, but the real element ids are auto-generated by Fabric when a data
# source is bound. R3 line 358 documents the retrieval mechanism: read them back from the
# live agent's definition via the ``getDefinition`` endpoint. The committed datasource.json
# files therefore carry schema-valid placeholder ids that keep offline assembly / dry-run
# valid, and at deploy time we fetch the real elements and re-inject them (see
# :func:`reconcile_datasource_elements`).


def get_data_agent_definition(token: str, workspace_id: str, agent_id: str) -> dict:
    """Retrieve a Data Agent's live item definition via the ``getDefinition`` endpoint.

    Returns the ``definition`` object (``{"parts": [...]}``) with Base64 ``payload``s.
    The call is a long-running operation (202 + Operation-Location); the result is
    returned by polling to completion (R3 §4.1 / line 358).
    """
    status, headers, payload = _request(
        "POST",
        f"{FABRIC_API_BASE}/workspaces/{workspace_id}/dataAgents/{agent_id}/getDefinition",
        token,
    )
    if status == 202:
        payload = _poll_long_running(headers, token) or payload
    definition = payload.get("definition", payload)
    if not isinstance(definition, dict):
        return {}
    return definition


def _decode_part_payload(part: dict) -> Optional[dict]:
    """Decode an InlineBase64 definition part payload into a dict (best-effort)."""
    payload = part.get("payload")
    if not payload or str(part.get("payloadType", "InlineBase64")) != "InlineBase64":
        return None
    try:
        raw = base64.b64decode(payload).decode("utf-8")
        return json.loads(raw)
    except (ValueError, json.JSONDecodeError):
        return None


def _collect_selected_display_names(elements: List[dict]) -> set:
    """Recursively collect the display_names that were marked ``is_selected`` in the
    committed (desired) ``elements`` tree, so the selection can be re-applied to the real
    elements retrieved from Fabric.
    """
    selected: set = set()
    for el in elements or []:
        if el.get("is_selected"):
            name = el.get("display_name")
            if name:
                selected.add(str(name))
        children = el.get("children")
        if isinstance(children, list):
            selected |= _collect_selected_display_names(children)
    return selected


def _apply_selection(elements: List[dict], desired_names: set) -> Tuple[List[dict], bool]:
    """Return a copy of *elements* (real ids preserved) with ``is_selected`` set from
    *desired_names*. A node is selected when its own display_name is desired OR any of its
    descendants is selected. When *desired_names* is empty every element is selected.
    """
    result: List[dict] = []
    any_selected = False
    for el in elements or []:
        node = dict(el)
        child_selected = False
        children = el.get("children")
        if isinstance(children, list):
            new_children, child_selected = _apply_selection(children, desired_names)
            node["children"] = new_children
        own = (not desired_names) or (str(el.get("display_name")) in desired_names)
        node["is_selected"] = bool(own or child_selected)
        any_selected = any_selected or node["is_selected"]
        result.append(node)
    return result, any_selected


def retrieve_datasource_elements(
    token: str, workspace_id: str, agent_id: str
) -> Dict[str, List[dict]]:
    """Retrieve the real ``DataSourceElement`` metadata Fabric auto-generated for each bound
    data source, keyed by per-source folder name (R3 line 358).

    Calls ``getDefinition`` on the live agent, decodes each ``draft/.../datasource.json``
    part, and returns ``{source_key: elements}`` with the real ids. The committed desired
    ``is_selected`` selection is re-applied (by display_name) onto the retrieved tree.
    Returns an empty mapping when nothing usable could be retrieved (e.g. tenant did not
    expose auto-generated elements), in which case the schema-valid placeholders are kept.
    """
    try:
        definition = get_data_agent_definition(token, workspace_id, agent_id)
    except DataAgentError as exc:
        LOG.warning(
            "Could not getDefinition for Data Agent %s (%s) - keeping placeholder "
            "DataSourceElement ids in the committed datasource.json.", agent_id, exc,
        )
        return {}

    retrieved: Dict[str, List[dict]] = {}
    for part in definition.get("parts", []) or []:
        path = str(part.get("path", ""))
        if not path.replace("\\", "/").endswith("/datasource.json"):
            continue
        # Only reconcile the draft stage; published is mirrored from it.
        if "/draft/" not in path.replace("\\", "/"):
            continue
        rel = path.split("Files/Config/", 1)[-1]
        source_key = _datasource_source_key(rel)
        if not source_key:
            continue
        decoded = _decode_part_payload(part)
        if not isinstance(decoded, dict):
            continue
        live_elements = decoded.get("elements")
        if not isinstance(live_elements, list) or not live_elements:
            continue
        desired = _collect_selected_display_names(_committed_elements(source_key))
        merged, _ = _apply_selection(live_elements, desired)
        retrieved[source_key] = merged
        LOG.info(
            "Retrieved %d real DataSourceElement(s) for %s via getDefinition.",
            len(live_elements), source_key,
        )
    if not retrieved:
        LOG.info(
            "getDefinition returned no auto-generated DataSourceElement metadata - keeping "
            "the committed schema-valid placeholder ids."
        )
    return retrieved


def _committed_elements(source_key: str) -> List[dict]:
    """Read the committed (desired) ``elements`` tree for a source key from the draft files."""
    rel = f"draft/{source_key}/datasource.json"
    try:
        obj = json.loads(_read_definition_file(rel))
    except (DataAgentError, json.JSONDecodeError):
        return []
    elements = obj.get("elements")
    return elements if isinstance(elements, list) else []


