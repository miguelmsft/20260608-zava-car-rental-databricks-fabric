def run(plan: DataAgentPlan, *, dry_run: bool = False) -> int:
    """Execute the Data Agent create/update flow. Returns an exit code."""
    if not plan.enable_data_agent:
        LOG.info(
            "features.enable_data_agent=false - skipping the Fabric Data Agent. "
            "Set enable_data_agent=true to enable Step 17."
        )
        return 0

    if dry_run:
        return _print_dry_run(plan)

    if not os.path.isdir(DEFINITION_ROOT):
        raise DataAgentError(f"Data Agent definition root not found: {DEFINITION_ROOT}")

    token = get_access_token()

    # 1. Resolve the Step-10 workspace.
    workspace_id = resolve_workspace_id(token, plan)

    # 2. Resolve the Step-14 semantic model (required grounding source).
    semantic_model_id, semantic_model_name = resolve_semantic_model(
        token, workspace_id, plan.semantic_model_name
    )

    # 3. Resolve the Step-16 ontology's auto-created Graph (optional graph source; R3/R4).
    graph: Optional[Tuple[str, str]] = None
    if plan.enable_ontology:
        graph = resolve_graph(token, workspace_id, plan.graph_name)
    else:
        LOG.info(
            "features.enable_ontology=false - grounding on the semantic model only. The graph "
            "source (the ontology's auto-created Graph, R4 §5.3) is the programmatic path to "
            "ontology-backed reasoning; attaching the ontology directly may be a UI step (R4)."
        )
    include_graph = graph is not None
    graph_id = graph[0] if graph else None
    graph_name = graph[1] if graph else None

    # 4. Build the item definition (tokens substituted; published stage mirrored; R3 §4.2/§9).
    parts = build_definition_parts(
        workspace_id=workspace_id,
        semantic_model_id=semantic_model_id,
        semantic_model_name=semantic_model_name,
        graph_id=graph_id,
        graph_name=graph_name,
        include_graph=include_graph,
    )
    definition = build_definition(parts)
    LOG.info(
        "Assembled %d definition part(s): semantic_model=%r%s.",
        len(parts), semantic_model_name,
        f" + graph={graph_name!r}" if include_graph else " (no graph source)",
    )

    # 5. Find-or-create the Data Agent item and deploy/refresh the definition (first pass:
    #    schema-valid placeholder DataSourceElement ids; binding the sources triggers
    #    Fabric to auto-generate the real element metadata).
    agent = find_or_create_data_agent(token, workspace_id, plan, definition)
    agent_id = str(agent.get("id") or "")

    # 6. Reconcile: retrieve the real DataSourceElement ids from the live agent
    #    (getDefinition; R3 line 358), inject them into the datasource parts, and re-deploy
    #    so the published definition carries real element ids instead of placeholders.
    reconciled = False
    if agent_id:
        reconciled = reconcile_datasource_elements(
            token,
            workspace_id,
            agent_id,
            semantic_model_id=semantic_model_id,
            semantic_model_name=semantic_model_name,
            graph_id=graph_id,
            graph_name=graph_name,
            include_graph=include_graph,
        )

    LOG.info(
        "Done. workspace_id=%s data_agent_id=%s sources=[semantic_model%s] elements=%s. "
        "Ask a sample question (e.g. 'average vehicle utilization across Seattle this month', "
        "'which sites have the most idle vehicles', 'top one-way flows') to validate grounding. "
        "R4: attaching the ontology directly (NL2Ontology) may be a UI step; this agent uses the "
        "graph source as the programmatic path.",
        workspace_id, agent.get("id"), ", graph" if include_graph else "",
        "real (reconciled via getDefinition)" if reconciled else "committed placeholders",
    )
    return 0


def _print_dry_run(plan: DataAgentPlan) -> int:
    """Credential-free preview of every intended REST call. No auth, no mutation."""
    LOG.info("[DRY-RUN] No authentication and no changes will be made.")
    LOG.info("[DRY-RUN] Config: %s", plan.config_path or "<none>")
    LOG.info("[DRY-RUN] enable_data_agent=%s enable_ontology=%s",
             plan.enable_data_agent, plan.enable_ontology)
    if not plan.enable_data_agent:
        LOG.info("[DRY-RUN] Feature gate off - nothing would be created.")
        return 0
    if plan.use_existing_workspace:
        LOG.info("[DRY-RUN] Would GET %s/workspaces/%s (existing).",
                 FABRIC_API_BASE, plan.existing_workspace_id)
    else:
        LOG.info("[DRY-RUN] Would GET %s/workspaces to resolve %r -> id.",
                 FABRIC_API_BASE, plan.workspace_name)
    LOG.info("[DRY-RUN] Would GET %s/workspaces/{id}/items?type=%s to resolve semantic model %r.",
             FABRIC_API_BASE, SEMANTIC_MODEL_ITEM_TYPE, plan.semantic_model_name)
    include_graph = plan.enable_ontology
    if include_graph:
        LOG.info("[DRY-RUN] Would GET %s/workspaces/{id}/items?type=%s to resolve the ontology's "
                 "auto-created Graph (name=%r or the single Graph item).",
                 FABRIC_API_BASE, GRAPH_ITEM_TYPE, plan.graph_name or "<auto>")
    else:
        LOG.info("[DRY-RUN] enable_ontology=false - graph source dropped; semantic-model only.")
    # Assemble the parts offline with placeholder ids to validate the definition structure.
    try:
        parts = build_definition_parts(
            workspace_id=ZERO_GUID,
            semantic_model_id=ZERO_GUID,
            semantic_model_name=plan.semantic_model_name,
            graph_id=ZERO_GUID if include_graph else None,
            graph_name=(plan.graph_name or "ZavaFleetOntologyGraph") if include_graph else None,
            include_graph=include_graph,
        )
        LOG.info("[DRY-RUN] Definition would have %d part(s):", len(parts))
        for part in parts:
            LOG.info("[DRY-RUN]   part: %s", part["path"])
    except DataAgentError as exc:
        LOG.warning("[DRY-RUN] could not assemble definition parts: %s", exc)
    LOG.info("[DRY-RUN] Would find-or-create Data Agent %r "
             "(POST %s/workspaces/{id}/dataAgents; InlineBase64 definition; idempotent "
             "updateDefinition on re-run).", plan.agent_name, FABRIC_API_BASE)
    LOG.info("[DRY-RUN] Would then getDefinition (POST %s/workspaces/{id}/dataAgents/{id}/"
             "getDefinition) to retrieve the real DataSourceElement ids Fabric auto-generates "
             "(R3 line 358), inject them into each datasource.json, and updateDefinition again "
             "so the published definition carries real element ids (not placeholders).",
             FABRIC_API_BASE)
    LOG.info("[DRY-RUN] Manual nuance (R4): attaching the ontology directly as an 'ontology' "
             "(NL2Ontology) source may be UI-only; the graph source is the programmatic path.")
    return 0


