def _is_placeholder(value: object) -> bool:
    return isinstance(value, str) and bool(_PLACEHOLDER_RE.match(value.strip()))


def _clean(value: object) -> Optional[str]:
    """Return a usable string value, or None for missing / blank / placeholder values."""
    if not isinstance(value, str):
        return None
    value = value.strip()
    if not value or _is_placeholder(value):
        return None
    return value


def _resolve_config_path(explicit: Optional[str]) -> Optional[str]:
    if explicit:
        if not os.path.isfile(explicit):
            raise DataAgentError(f"--config path does not exist: {explicit}")
        return explicit
    for candidate in DEFAULT_CONFIG_CANDIDATES:
        if os.path.isfile(candidate):
            return candidate
    return None


def _load_config(path: Optional[str]) -> dict:
    if not path:
        return {}
    try:
        with open(path, "r", encoding="utf-8") as fh:
            data = json.load(fh)
    except (OSError, json.JSONDecodeError) as exc:
        raise DataAgentError(f"failed to read config {path}: {exc}") from exc
    if not isinstance(data, dict):
        raise DataAgentError(f"config {path} must be a JSON object")
    return data


class DataAgentPlan:
    """Resolved, validated plan for the Data Agent operation (no secrets)."""

    def __init__(
        self,
        *,
        config_path: Optional[str],
        workspace_name: Optional[str],
        use_existing_workspace: bool,
        existing_workspace_id: Optional[str],
        semantic_model_name: str,
        graph_name: Optional[str],
        agent_name: str,
        enable_data_agent: bool,
        enable_ontology: bool,
    ) -> None:
        self.config_path = config_path
        self.workspace_name = workspace_name
        self.use_existing_workspace = use_existing_workspace
        self.existing_workspace_id = existing_workspace_id
        self.semantic_model_name = semantic_model_name
        self.graph_name = graph_name
        self.agent_name = agent_name
        self.enable_data_agent = enable_data_agent
        self.enable_ontology = enable_ontology


def resolve_plan(
    *,
    config_path: Optional[str] = None,
    workspace_name: Optional[str] = None,
    existing_workspace_id: Optional[str] = None,
    semantic_model_name: Optional[str] = None,
    graph_name: Optional[str] = None,
    agent_name: Optional[str] = None,
) -> DataAgentPlan:
    """Resolve the Data Agent operation plan from config + CLI/env overrides.

    Precedence per field: explicit CLI arg > config value > environment variable.
    Raises ``DataAgentError`` naming any field that could not be resolved.
    """
    resolved_path = _resolve_config_path(config_path)
    cfg = _load_config(resolved_path)
    ws_cfg = cfg.get("workspace", {}) if isinstance(cfg.get("workspace"), dict) else {}
    sm_cfg = cfg.get("semantic_model", {}) if isinstance(cfg.get("semantic_model"), dict) else {}
    ont_cfg = cfg.get("ontology", {}) if isinstance(cfg.get("ontology"), dict) else {}
    da_cfg = cfg.get("data_agent", {}) if isinstance(cfg.get("data_agent"), dict) else {}
    feat_cfg = cfg.get("features", {}) if isinstance(cfg.get("features"), dict) else {}

    # Feature gates. enable_data_agent defaults true (the "beyond Power BI" pillar).
    # enable_ontology toggles whether the graph source (ontology's auto Graph) is wired in.
    flag = feat_cfg.get("enable_data_agent")
    enable_data_agent = bool(flag) if isinstance(flag, bool) else True
    ont_flag = feat_cfg.get("enable_ontology")
    enable_ontology = bool(ont_flag) if isinstance(ont_flag, bool) else False

    use_existing = bool(ws_cfg.get("use_existing"))
    name = workspace_name or _clean(ws_cfg.get("name")) or os.environ.get("FABRIC_WORKSPACE_NAME")
    existing_id = (
        existing_workspace_id
        or _clean(ws_cfg.get("existing_workspace_id"))
        or os.environ.get("FABRIC_WORKSPACE_ID")
    )

    sm_name = (
        semantic_model_name
        or _clean(da_cfg.get("semantic_model_name"))
        or _clean(sm_cfg.get("name"))
        or os.environ.get("FABRIC_SEMANTIC_MODEL_NAME")
        or DEFAULT_SEMANTIC_MODEL_NAME
    )
    # Graph display name: explicit override, else config, else the ontology name
    # (the auto-created Graph commonly shares the ontology's name); None => resolve the
    # single Graph item in the workspace.
    graph = (
        graph_name
        or _clean(da_cfg.get("graph_name"))
        or _clean(ont_cfg.get("graph_name"))
        or _clean(ont_cfg.get("name"))
        or os.environ.get("FABRIC_GRAPH_NAME")
    )
    agent = (
        agent_name
        or _clean(da_cfg.get("name"))
        or os.environ.get("FABRIC_DATA_AGENT_NAME")
        or DEFAULT_DATA_AGENT_NAME
    )

    # Validation.
    if use_existing:
        if not existing_id:
            raise DataAgentError(
                "workspace.use_existing=true but no workspace id resolved "
                "(set workspace.existing_workspace_id, --workspace-id, or FABRIC_WORKSPACE_ID)"
            )
        if not _GUID_RE.match(existing_id):
            raise DataAgentError(f"resolved existing workspace id is not a GUID: {existing_id!r}")
    elif not name:
        raise DataAgentError(
            "could not resolve workspace name "
            "(set workspace.name, --workspace-name, or FABRIC_WORKSPACE_NAME)"
        )

    if not sm_name:
        raise DataAgentError(
            "could not resolve the semantic model name "
            "(set semantic_model.name, --semantic-model-name, or FABRIC_SEMANTIC_MODEL_NAME)"
        )
    if not agent:
        raise DataAgentError(
            "could not resolve the Data Agent display name "
            "(set data_agent.name, --agent-name, or FABRIC_DATA_AGENT_NAME)"
        )

    return DataAgentPlan(
        config_path=resolved_path,
        workspace_name=name,
        use_existing_workspace=use_existing,
        existing_workspace_id=existing_id,
        semantic_model_name=sm_name,
        graph_name=graph,
        agent_name=agent,
        enable_data_agent=enable_data_agent,
        enable_ontology=enable_ontology,
    )


